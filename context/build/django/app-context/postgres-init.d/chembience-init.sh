#!/bin/bash
set -e

echo "$POSTGRES_USER :: $POSTGRES_DB :: $CHEMBIENCE_DB_USER :: $CHEMBIENCE_DB_USER :: $CHEMBIENCE_DB_PASSWORD"
psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
    CREATE USER chembience SUPERUSER;
    CREATE DATABASE $CHEMBIENCE_DB_USER;
    GRANT ALL PRIVILEGES ON DATABASE $CHEMBIENCE_DB_USER TO $CHEMBIENCE_DB_USER;
    ALTER USER chembience with encrypted password '$CHEMBIENCE_DB_PASSWORD';
EOSQL

psql -v ON_ERROR_STOP=1 --username "$CHEMBIENCE_DB_USER" --dbname "$CHEMBIENCE_DB_USER" <<-EOSQL
    CREATE EXTENSION IF NOT EXISTS rdkit;
EOSQL

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
    ALTER USER chembience NOSUPERUSER;
EOSQL