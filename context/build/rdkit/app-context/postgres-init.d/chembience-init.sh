#!/bin/bash
set -e

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
    CREATE USER chembience SUPERUSER;
    CREATE DATABASE $CHEMBIENCE_DB_NAME;
    GRANT ALL PRIVILEGES ON DATABASE $CHEMBIENCE_DB_NAME TO $CHEMBIENCE_DB_USER;
    ALTER USER chembience with encrypted password '$CHEMBIENCE_DB_PASSWORD';
EOSQL

psql -v ON_ERROR_STOP=1 --username "$CHEMBIENCE_DB_USER" --dbname "$CHEMBIENCE_DB_NAME" <<-EOSQL
    CREATE EXTENSION IF NOT EXISTS rdkit;
EOSQL

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
    ALTER USER chembience NOSUPERUSER CREATEDB;
EOSQL